@tool
async def search_faq(query: str, top_k: int = 5) -> str:
    """检索中国入境游知识库。

    覆盖签证政策、城市指南、景点信息、美食推荐、交通方式、预算参考、
    文化礼仪、应急信息、节假日等。用于回答客户关于中国旅游的各类问题。

    Args:
        query: 搜索关键词或问题 (中文或英文)
        top_k: 返回结果数量上限 (默认5)

    Returns:
        JSON 格式的相关知识条目
    """
    import json

    results = []

    # 精确匹配
    for key, value in _FAQ_DB.items():
        if key in query or query.lower() in key.lower():
            results.append({"title": key, "content": value, "score": 1.0})

    # 模糊匹配 (内容中包含)
    for key, value in _FAQ_DB.items():
        if key not in [r["title"] for r in results]:
            # 检查查询词是否出现在内容或标题中
            score = 0.0
            if query.lower() in value.lower():
                score = 0.7
            # 部分关键词匹配
            for word in query.split():
                if len(word) >= 2 and word.lower() in value.lower():
                    score = max(score, 0.4)
            if score > 0:
                results.append({"title": key, "content": value, "score": score})

    # 按分数排序
    results.sort(key=lambda x: x["score"], reverse=True)
    results = results[:top_k]

    # 如果没匹配到，返回默认帮助
    if not results:
        results = [{
            "title": "帮助",
            "content": "未找到精确匹配。请尝试搜索：签证、免签、北京、上海、西安、成都、桂林、云南、张家界、杭州、苏州、重庆、广州、厦门、哈尔滨、拉萨、线路推荐、预算、交通、天气、支付、上网、礼仪、紧急等关键词。",
            "score": 0.0,
        }]

    return json.dumps(results, ensure_ascii=False, indent=2)
